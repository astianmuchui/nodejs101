const os = require('os')

// Platform
console.log(os.platform())

// CPU Arch

console.log(os.arch())

// CPU Core info 

console.log(os.cpus())

// Free memory 
console.log(os.freemem())

// Total Memory 
console.log(os.totalmem())

// Home directory 
console.log(os.homedir())

// UPTIME 
console.log(os.uptime())